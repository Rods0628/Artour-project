                        <h2 class="text-white">TIAONG QUEZON</h2>
                        <p class=" py-3">
                           In the Philippines, the town of Tiaong is a municipality of the first class located in the province of Quezon. Its official name is the Municipality of Tiaong (Tagalog: Bayan ng Tiaong). The population was counted at 106,265 at the census that was taken in the year 2020.
                        </p>
                        
                        <p class="py-3">
                           More than 300 years ago, Tiaong was just a part of the province of Laguna's municipality of San Pablo, which is now a city. It is bounded to the south by San Antonio, Quezon, to the north by San Pablo City, to the northeast by Dolores, Quezon, to the west by Lipa City, and to the east by Candelaria, Quezon. Tiaong became its own municipality in 1961, thanks to the work of a woman named "Lalig" who lived on the town's edge and cared about helping people, was patriotic, and was very religious. She was known as "Donya Tating" because her usual job was to ride a cart pulled by a big black bull to church every day. People loved and respected her because she was kind to the poor, especially. Even the parish priest wouldn't start the mass until he heard the loud "Ooooong" of the bull as he walked into the churchyard.
                        </p>
                        <p class="m-0">
                            People in the town loved to call Donya Tating "Tiya" as a sign of their gratitude and affection for her (Auntie). The town of Tiaong got its name from the combination of the name "Tiya" and the sound "Ooooong" makes when pronounced together. This town will now be called Tiaong (Tiya-ong).
                        </p>
                        <p class="py-3">
                            The town proper was then built along a river called "Malaking Ilog," which is 4 km south of where it is now. Pirates who went on raids often attacked the Poblacion, setting houses on fire, killing people and animals, and destroying property. People moved the Poblacion to where it is now because the image of St. John the Baptist showed up there several times. People dug deep trenches around the town and put up sharp bamboo stakes to protect it. A stone watchtower was built to keep an eye out for and scare away pirates. During the time of the Spanish, the Governadorcillo was in charge of the local government. He was helped by the Tenyente Mayor, the Juez de Sementera, the Juez de Ganados, the Juez de Palayan, and the Juez de Polisya. They were chosen by nine important, powerful, and authoritative people, with the help of the Governador General.
                        </p>
                        <p class="m-0">
                        Don Francisco Magdami was the first Gobernadorcillo. This was in 1691. Franciscan Missionaries built the church in 1670. It is northeast of the town and faces what is now called Escudero Street. This is one of the province of Tayabas's oldest, longest, and biggest churches (now Quezon). Most of the people who live in the town are Roman Catholic, but there are also Protestants, Iglesia Ni Cristo members, "Sabadistas," Esperetistas, and members of other religious groups.
                    </p>
                         <p class="py-3">
                       Most of the people in Tiaong are Batanguenos, who come from the province of Batangas, especially the towns of San Jose, Ibaan, Rosario, Bauan, Taal, and Lipa City. Tiaong is about 15,239 hectares in size (16,838 unofficial and not yet confirmed). Originally there are only 14 barrios namely: Bana, Bula, Bulakin, Cabatang, Cabay, Lagalag, Lalig, Lusacan, Paiisa, Quipot, Buliran, Callejon, Niing and Pury. When Buliran became its own town on October 15, 1957, the last four barrios were split off. The town was then called San Antonio, Quezon. Candelaria may have been a part of Tiaong in the past, but Dolores was split off from the town of San Pablo and made into a municipality in 1879. During the American rule, however, Dolores became a "barrio" of Tiaong in 1901. After nine years, on April 11, 1910, Dolores became a municipality again.
                    </p>
                    <p class="m-0">
                    The friars set up the first school, which was in a building called a convent. Men between the ages of 7 and 14 were the only ones who could go. Kartilya Kristiano, Trisagio, Holy Rosary, and Infants Manual were taught. These books can only be printed with the Archbishop of Manila's permission. After 50 years, female students of the same age were allowed to sign up. This was because the Governor General told the friars not to marry people unless they knew how to pray.
                </p>
                  <p class="py-3">
                    From 1690 to 1790, farming was the main way people made a living and made a living. The Juez de Sementera told the people to grow palay, corn, mongo, and other crops. At the same time, people were encouraged to raise animals like horses, cows, and carabaos. Most men worked as blacksmiths to make tools for gardening and building, while women did things like weaving and made flour from cassava, uraro, and mani. Some women also made oil from coconut and lumbang seeds. People were also encouraged to cook supplemental foods like rice cakes, suman, etc., and treats like panutsa, bukayo, etc.
                </p>
                <p class="m-0">
                During the Spanish rule, Andres Bonifacio set up a secret revolutionary group called the K.K.K. It was part of the Tiaong and was led by brave nationalists like Ladislao Masangkay. They fought against the Spaniards and the municipality. This group kept doing brave things right up until the Americans took over.
            </p>

                <p class="py-3">
                On January 14, 1900, American soldiers led by General Schwan invaded China and took control of the city of Tiaong. William Howard Taftas was the first Civil Governor of the island, and Don Pedro P. Cantos was named the First Municipal President of Tiaong. The United States set up a civil government in the Philippines.
            </p>

